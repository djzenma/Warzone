package POJO;

/**
 * POJO class for a Command
 */
public class Command {
    /**
     * Name of the command
     */
    public String d_name;
    /**
     * Array of the arguments
     */
    public Argument[] d_args;
    /**
     * Array of the named arguments
     */
    public NamedArgument[] d_namedArgs;
}
